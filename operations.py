
def binary_hex(binary):
    ans = hex(binary)
    return ans[2:]


def binary_octal(binary):
    ans = oct(binary)
    return ans[2:]


def octal_binary(octal):
    ans = bin(octal)
    return ans[2:]


def decimal_binary(decimal):
    ans = bin(decimal)
    return ans[2:]


def hex_binary(hex):
    ans = bin(hex)
    return ans[2:]
